package github.vrih.xsub.util;

import java.io.Serializable;

/**
 * @author Sindre Mehus
 */
public class Pair<S, T> implements Serializable {

    private S first;
    private T second;

    public Pair() {
    }

    public Pair(S first, T second) {
        this.first = first;
        this.second = second;
    }

    public S getFirst() {
        return first;
    }

    public void setFirst(S first) {
        this.first = first;
    }

    public T getSecond() {
        return second;
    }

    public void setSecond(T second) {
        this.second = second;
    }
}
